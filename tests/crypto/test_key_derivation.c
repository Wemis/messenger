#include <math.h>
#include <string.h>
#include <stdarg.h>
#include <stddef.h>
#include <stdint.h>
#include <setjmp.h>
#include <cmocka.h>

#include "crypto/key_derivation.h"
#include "shared/result.h"

static void test_key_derivation_same_seed() {
    const uint8_t master_key[32] = {
        0x03, 0xac, 0x67, 0x42, 0x16, 0xf3, 0xe1, 0x5c,
        0x76, 0x1e, 0xe1, 0xa5, 0xe2, 0x55, 0xf0, 0x67,
        0x95, 0x36, 0x23, 0xc8, 0xb3, 0x88, 0xb4, 0x45,
        0x9e, 0x13, 0xf9, 0x78, 0xd7, 0xc8, 0x46, 0xf4
    };
    
    const uint8_t context[32] = {
        0xf0, 0xad, 0x3c, 0x42, 0x16, 0xf3, 0xe1, 0x5c,
        0x76, 0x1e, 0x8b, 0x78, 0xe2, 0x55, 0xf0, 0x67,
        0x95, 0x36, 0x23, 0xc8, 0xb3, 0x88, 0x91, 0x45,
        0x65, 0x13, 0xac, 0x71, 0xd7, 0xc8, 0x46, 0x04
    };
    
    uint8_t key1[32];
    uint8_t key2[32];

    result_t r1 = key_expand(key1, 32, context, 32, master_key);
    result_t r2 = key_expand(key2, 32, context, 32, master_key);

    assert_int_equal(r1, OK);
    assert_int_equal(r2, OK);
    assert_memory_equal(key1, key2, 32);
}

static void test_key_derivation_different_contexts() {
    const uint8_t master_key[32] = {
        0x03, 0xac, 0x67, 0x42, 0x16, 0xf3, 0xe1, 0x5c,
        0x76, 0x1e, 0xe1, 0xa5, 0xe2, 0x55, 0xf0, 0x67,
        0x95, 0x36, 0x23, 0xc8, 0xb3, 0x88, 0xb4, 0x45,
        0x9e, 0x13, 0xf9, 0x78, 0xd7, 0xc8, 0x46, 0xf4
    };
    
    const uint8_t context1[32] = {
        0xf0, 0xad, 0x3c, 0x42, 0x16, 0xf3, 0xe1, 0x5c,
        0x76, 0x1e, 0x8b, 0x78, 0xe2, 0x55, 0xf0, 0x67,
        0x95, 0x36, 0x23, 0xc8, 0xb3, 0x88, 0x91, 0x45,
        0x65, 0x13, 0xac, 0x71, 0xd7, 0xc8, 0x46, 0x04
    };
    
    const uint8_t context2[32] = {
        0x91, 0xad, 0x3c, 0x42, 0x16, 0xc0, 0xe1, 0x5c,
        0x76, 0x11, 0x8b, 0xcc, 0xe2, 0x55, 0xf0, 0x67,
        0x96, 0x36, 0x23, 0xc8, 0xb3, 0x68, 0x91, 0x6f,
        0x65, 0x13, 0xac, 0x02, 0xee, 0xc8, 0x46, 0x04
    };
     
    uint8_t key1[32];
    uint8_t key2[32];

    result_t r1 = key_expand(key1, 32, context1, 32, master_key);
    result_t r2 = key_expand(key2, 32, context2, 32, master_key);

    assert_int_equal(r1, OK);
    assert_int_equal(r2, OK);
    assert_memory_not_equal(key1, key2, 32);
}

int main(void) {
    const struct CMUnitTest tests[] = {
        cmocka_unit_test(test_key_derivation_same_seed),
        cmocka_unit_test(test_key_derivation_different_contexts)
    };

    return cmocka_run_group_tests(tests, NULL, NULL);
}
